create function public.fhec_registrar_notificaciones_banner_20260722()
returns trigger
language plpgsql
security definer
set search_path = public, auth
as $$
begin
    insert into public.notificaciones (
        id_usuario,
        id_banner,
        titulo,
        mensaje,
        tipo_notificacion,
        canales_solicitados,
        correo_destino,
        telefono_destino
    )
    select
        u.id,
        new.id_banner,
        new.titulo,
        coalesce(nullif(trim(new.subtitulo), ''), new.titulo),
        'promocional',
        array_remove(array[
            'in_app',
            case
                when coalesce(u.acepta_promociones_correo, false)
                 and coalesce(u.acepta_notificaciones_correo, false)
                 and au.email is not null then 'correo'
            end,
            case
                when coalesce(u.acepta_promociones_sms, false)
                 and coalesce(u.acepta_notificaciones_sms, false)
                 and u.telefono is not null then 'sms'
            end
        ], null),
        case
            when coalesce(u.acepta_promociones_correo, false)
             and coalesce(u.acepta_notificaciones_correo, false) then au.email
        end,
        case
            when coalesce(u.acepta_promociones_sms, false)
             and coalesce(u.acepta_notificaciones_sms, false) then concat(coalesce(u.codigo_area, ''), u.telefono)
        end
    from public.usuarios u
    left join auth.users au on au.id = u.id
    where coalesce(u.acepta_promociones, false)
      and coalesce(u.acepta_notificaciones, false);

    return new;
end;
$$;

create trigger banners_registrar_notificaciones_20260722
after insert on public.banners
for each row execute function public.fhec_registrar_notificaciones_banner_20260722();

