-- Migracion aditiva y transaccional.
-- No elimina ni reemplaza objetos existentes. Si encuentra un conflicto, aborta
-- antes de crear nada y PostgreSQL revierte toda la transaccion.
begin;

do $$
declare
    missing_columns text[];
begin
    if to_regclass('public.usuarios') is null then
        raise exception 'Migracion cancelada: no existe public.usuarios.';
    end if;

    if to_regclass('public.banners') is not null
       or to_regclass('public.notificaciones') is not null then
        raise exception 'Migracion cancelada: banners o notificaciones ya existe. No se modifico ninguna tabla.';
    end if;

    if to_regprocedure('public.fhec_registrar_notificaciones_banner_20260722()') is not null then
        raise exception 'Migracion cancelada: la funcion de notificaciones ya existe.';
    end if;

    select array_agg(required.column_name)
    into missing_columns
    from unnest(array[
        'id',
        'rol',
        'telefono',
        'codigo_area',
        'acepta_promociones',
        'acepta_promociones_sms',
        'acepta_promociones_correo',
        'acepta_notificaciones',
        'acepta_notificaciones_sms',
        'acepta_notificaciones_correo'
    ]) as required(column_name)
    where not exists (
        select 1
        from information_schema.columns c
        where c.table_schema = 'public'
          and c.table_name = 'usuarios'
          and c.column_name = required.column_name
    );

    if missing_columns is not null then
        raise exception 'Migracion cancelada: faltan columnas en usuarios: %', array_to_string(missing_columns, ', ');
    end if;
end;
$$;

create table public.banners (
    id_banner bigint generated by default as identity primary key,
    titulo text not null check (char_length(trim(titulo)) between 1 and 120),
    subtitulo text,
    etiqueta text,
    texto_accion text,
    url_accion text,
    url_imagen text not null,
    color_inicio text not null default '#031b24',
    color_medio text not null default '#00546a',
    color_fin text not null default '#50e9f8',
    fecha_creacion timestamptz not null default now()
);

create table public.notificaciones (
    id_notificacion bigint generated by default as identity primary key,
    id_usuario uuid not null references public.usuarios(id) on delete cascade,
    id_banner bigint references public.banners(id_banner) on delete set null,
    titulo text not null,
    mensaje text not null,
    fecha_notificacion timestamptz not null default now(),
    leida boolean not null default false,
    tipo_notificacion text not null default 'promocional',
    canales_solicitados text[] not null default array['in_app']::text[],
    canales_enviados text[] not null default array['in_app']::text[],
    estado_envio jsonb not null default '{"in_app":"enviado"}'::jsonb,
    correo_destino text,
    telefono_destino text,
    constraint notificaciones_usuario_banner_unique unique (id_usuario, id_banner)
);

create index notificaciones_usuario_fecha_idx
    on public.notificaciones (id_usuario, fecha_notificacion desc);
create index notificaciones_banner_idx
    on public.notificaciones (id_banner) where id_banner is not null;

